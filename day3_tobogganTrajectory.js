let strings = input.split('\n');

function howManyTrees(x, y) {
  let numberOfTrees = 0;
  let position = x;
  const rowLength = strings[0].length;

  for(let i = y; i <= strings.length - 1; i = (i+y)) {
    if (strings[i][position] === "#") {
      numberOfTrees++;
    }
    position = ((position + x) % rowLength);
  }

  return numberOfTrees;
}

console.log(howManyTrees(3, 1));

const slopes = [
  [1,1], 
  [3,1], 
  [5,1], 
  [7,1], 
  [1,2], 
]

function howManyTreesForMultipleSlopes() {
  let allTrees = [];

  for(let j = 0; j < slopes.length; j++) {
    allTrees.push(howManyTrees(slopes[j][0], slopes[j][1]));
  }

  return allTrees.reduce((a, b)=> a*b, 1);
}

console.log(howManyTreesForMultipleSlopes());
